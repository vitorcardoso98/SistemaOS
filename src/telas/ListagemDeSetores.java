    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        limparCampos();
        btnSalvar.setEnabled(false);
        if (evt.isMetaDown()) {
            jPopupMenu1.show(jTable1, evt.getX(), evt.getY());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este setor?");
        int linha = jTable1.getSelectedRow();
        if (opcao == 0) {
            Setor setor = new Setor();
            setor.setIdSetor(Integer.parseInt(jTable1.getValueAt(linha, 0).toString()));
            try {
                setorDAO.excluir(setor);
                JOptionPane.showMessageDialog(null, "Setor excluído com sucesso!");
                preencherTabelaSetores();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        int linha = jTable1.getSelectedRow();
        try {
            Setor setor = setorDAO.consultaPorSetor(Integer.parseInt(jTable1.getValueAt(linha, 0).toString()));
            txtCodigo.setText(String.valueOf(setor.getIdSetor()));
            txtNome.setText(setor.getNome());
            txtEndereco.setText(setor.getEndereco());
            txtResponsavel.setText(setor.getResponsavel());
            txtTelefone.setText(setor.getTelefone());
            btnSalvar.setEnabled(true);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            Setor setor = new Setor();
            setor.setIdSetor(Integer.parseInt(txtCodigo.getText()));
            setor.setNome(txtNome.getText());
            setor.setEndereco(txtEndereco.getText());
            setor.setResponsavel(txtResponsavel.getText());
            setor.setTelefone(txtTelefone.getText());
            setorDAO.editarSetor(setor);
            
            JOptionPane.showMessageDialog(null, "Setor editado com sucesso!");
            preencherTabelaSetores();
            limparCampos();
            btnSalvar.setEnabled(false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed


