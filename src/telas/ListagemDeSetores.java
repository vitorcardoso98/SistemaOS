    public void preencherTabelaSetores() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setNumRows(0);
        ArrayList<Setor> setores = setorDAO.listarSetores();
        for (int i = 0; i < setores.size(); i++) {
            dtm.addRow(new Object[]{
                setores.get(i).getIdSetor(),
                setores.get(i).getNome(),
                setores.get(i).getEndereco(),
                setores.get(i).getResponsavel()
            });
        }
    }
    
